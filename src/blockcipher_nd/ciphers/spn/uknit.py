def _int_to_cells(value: int, cells: int) -> tuple[int, ...]:
    return tuple((value >> (4 * (cells - 1 - index))) & 0xF for index in range(cells))


def _cells_to_int(cells: tuple[int, ...]) -> int:
    value = 0
    for cell in cells:
        value = (value << 4) | cell
    return value


def _diffuse_key(cells: tuple[int, ...]) -> tuple[int, ...]:
    result = list(cells)
    for target, source in ((3, 0), (7, 4), (11, 8), (15, 12)):
        result[target] ^= result[source]
    return tuple(result)


def _permute_key(cells: tuple[int, ...]) -> tuple[int, ...]:
    return tuple(cells[source] for source in UKNIT_KEY_PERMUTATION)


def uknit_round_keys(key: int) -> tuple[int, ...]:
    if not isinstance(key, int):
        raise TypeError("uKNIT-BC key must be an integer")
    if key < 0 or key >= 1 << 128:
        raise ValueError("uKNIT-BC key must fit in 128 bits")
    master = _int_to_cells(key, 32)
    first = master[:16]
    second = master[16:]
    first_state = first
    second_state = second
    keys = [_cells_to_int(first), _cells_to_int(second)]
    for _ in range(2, 13):
        first_state = _permute_key(_diffuse_key(first_state))
        second_state = _permute_key(
            _diffuse_key(tuple(UKNIT_KEY_LFSR[cell] for cell in second_state))
        )
        keys.append(
            _cells_to_int(
                tuple(
                    left ^ right
                    for left, right in zip(first_state, second_state, strict=True)
                )
            )
        )
    return tuple(keys)


def uknit_substitution_layer(state: int, round_index: int) -> int:
    if round_index < 0 or round_index >= UKNIT_ROUNDS:
        raise ValueError("uKNIT-BC S-box round index must be in [0, 11]")
    cells = _int_to_cells(state, 16)
    tables = UKNIT_SBOX_TABLES[round_index]
    return _cells_to_int(
        tuple(tables[15 - index][cell] for index, cell in enumerate(cells))
    )


def uknit_linear_layer(state: int, round_index: int) -> int:
    if round_index < 0 or round_index >= UKNIT_TRANSITION_ROUNDS:
        raise ValueError("uKNIT-BC linear round index must be in [0, 10]")
    result = 0
    for target, sources in enumerate(UKNIT_LINEAR_TARGET_SOURCES[round_index]):
        bit = 0
        for source in sources:
            bit ^= (state >> source) & 1
        result |= bit << target
    return result


@dataclass(frozen=True)
class UknitBc:
    """64-bit uKNIT-BC with prefix-reduced semantics for rounds 1 through 11."""

    rounds: int = UKNIT_ROUNDS
    key: int = 0

    name: str = "uKNIT-BC"
    structure: str = "SPN"
    block_bits: int = 64
    key_bits: int = 128

    def __post_init__(self) -> None:
        if not isinstance(self.rounds, int):
            raise TypeError("rounds must be an integer")
        if self.rounds < 1 or self.rounds > UKNIT_ROUNDS:
            raise ValueError("uKNIT-BC supports 1..12 rounds")
        if not isinstance(self.key, int):
            raise TypeError("key must be an integer")
        if self.key < 0 or self.key >= 1 << self.key_bits:
            raise ValueError("uKNIT-BC key must fit in 128 bits")

    def encrypt(self, plaintext: int) -> int:
        if not isinstance(plaintext, int):
            raise TypeError("plaintext must be an integer")
        if plaintext < 0 or plaintext >= 1 << self.block_bits:
            raise ValueError("uKNIT-BC plaintext must fit in 64 bits")

        round_keys = uknit_round_keys(self.key)
        state = plaintext
        for round_index in range(min(self.rounds, UKNIT_TRANSITION_ROUNDS)):
            state ^= round_keys[round_index]
            state = uknit_substitution_layer(state, round_index)
            state = uknit_linear_layer(state, round_index)
        if self.rounds == UKNIT_ROUNDS:
            state ^= round_keys[11]
            state = uknit_substitution_layer(state, 11)
            state ^= round_keys[12]
        return state


